#Se e maior
print(9>5) #Verdadeiro
print(9>15) #Falso


#Se e maior ou igual
print(9>=9) #Verdadeiro
print(9>=10) #Falso


#Se e menor
print(9<10) #Verdadeiro
print(15<10) #False


#Se e menor ou igual
print(9<=10) #Verdadeiro
print(10<=10) #Verdadeiro
print(15<=10) #Falso


#Se e igual
print(9==10) #Falso
print(10==10) #Verdadeiro


#Se e diferente
print(9!=10) #Verdadeiro
print(10!=10) #Falso


'''Ordem alfabetica ''' 

print("Andre"<"Jose") #Verdadeiro
print("Andre">"Jose") #Falso
print("Andre"<="Jose") #Verdadeiro




